import { Component, OnInit } from '@angular/core';
import { ActivatedRoute } from '@angular/router';
import { JhiDataUtils } from 'ng-jhipster';

import { IAdvertisement } from 'app/shared/model/advertisement.model';

@Component({
  selector: 'jhi-advertisement-detail',
  templateUrl: './advertisement-detail.component.html',
})
export class AdvertisementDetailComponent implements OnInit {
  advertisement: IAdvertisement | null = null;

  constructor(protected dataUtils: JhiDataUtils, protected activatedRoute: ActivatedRoute) {}

  ngOnInit(): void {
    this.activatedRoute.data.subscribe(({ advertisement }) => (this.advertisement = advertisement));
  }

  byteSize(base64String: string): string {
    return this.dataUtils.byteSize(base64String);
  }

  openFile(contentType = '', base64String: string): void {
    this.dataUtils.openFile(contentType, base64String);
  }

  previousState(): void {
    window.history.back();
  }
}
